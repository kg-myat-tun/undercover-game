export function t(
  locale: AppLocale,
  key: keyof typeof messages.en,
  vars?: Vars,
): string {
  const template = messages[locale][key] ?? messages.en[key];
  if (!vars) {
    return template;
  }

  return Object.entries(vars).reduce(
    (output, [name, value]) => output.replaceAll(`{${name}}`, String(value)),
    template,
  );
}

